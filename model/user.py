class User:
    def __init__(self, first_name, last_name, address, postal_code, city, email, phone, password):
        self.first_name = first_name
        self.last_name = last_name
        self.address = address
        self.postal_code = postal_code
        self.city = city
        self.email = email
        self.phone = phone
        self.password = password

